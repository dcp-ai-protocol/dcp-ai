# 4. Domain-Separated Messages Agreement
# ---------------------------------------------------------------------------


class TestDomainSeparation:
    def test_all_contexts_distinct(self, V):
        payload = V["domain_separation_all_contexts"]["payload_canonical"].encode("utf-8")
        dsm_set = set()
        for ctx_name, ctx_val in DCP_CONTEXTS.items():
            dsm = domain_separated_message(ctx_val, payload)
            hex_val = dsm.hex()
            expected = V["domain_separation_all_contexts"]["dsm_hex"][ctx_name]
            assert hex_val == expected, f"DSM mismatch for {ctx_name}"
            dsm_set.add(hex_val)
        assert len(dsm_set) == len(DCP_CONTEXTS)


# ---------------------------------------------------------------------------
# 5. Ed25519 Signature Verification
# ---------------------------------------------------------------------------


class TestEd25519Signatures:
    @pytest.mark.asyncio
    @pytest.mark.parametrize("sig_name", ["passport_ed25519", "intent_ed25519", "audit_ed25519"])
    async def test_verify(self, V, registry, sig_name):
        entry = V["ed25519_signatures"][sig_name]
        canonical = V["canonicalization"][entry["payload_key"]]["expected_canonical"]
        payload_bytes = canonical.encode("utf-8")
        dsm = domain_separated_message(entry["context"], payload_bytes)
        sig_bytes = base64.b64decode(entry["sig_b64"])
        pub_key_b64 = V["test_keys"]["ed25519"]["public_key_b64"]
        ed = Ed25519Provider()
        valid = await ed.verify(dsm, sig_bytes, pub_key_b64)
        assert valid is True

    @pytest.mark.asyncio
    async def test_wrong_key_fails(self, V, registry):
        entry = V["ed25519_signatures"]["passport_ed25519"]
        canonical = V["canonicalization"][entry["payload_key"]]["expected_canonical"]
        dsm = domain_separated_message(entry["context"], canonical.encode("utf-8"))
        sig_bytes = base64.b64decode(entry["sig_b64"])
        ed = Ed25519Provider()
        wrong_kp = await ed.generate_keypair()
        valid = await ed.verify(dsm, sig_bytes, wrong_kp["public_key_b64"])
        assert valid is False


