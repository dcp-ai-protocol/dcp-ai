"""
DCP-AI v2.0 Cross-Language Interoperability Tests (Python SDK)

Verifies golden vectors generated by the TypeScript SDK. Covers kid
derivation, canonicalization, domain separation, Ed25519 signature
verification, composite (Ed25519 + ML-DSA-65) signature verification,
stripping attack resistance, cross-context replay, and session splicing.

The same interop_vectors.json is consumed by TS, Go, and Rust SDKs
to form the 4×4 cross-language verification matrix.
"""

from __future__ import annotations

import base64
